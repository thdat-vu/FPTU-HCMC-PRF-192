#include<stdio.h>
main(){
    printf("---------------------------------------------------------------\n");
    printf("%d|<--              %%d\n",4321);
    printf("%10d|<--              %%10d\n",4321);
    printf("%010d|<--              %%010d\n",4321);
    printf("%-10d|<--              %%-10d\n",4321);
    printf("---------------------------------------------------------------\n");
    printf("%lf|<--              %%d\n",4321);
    printf("%10.3lf|<--              %%10d\n",4321);
    printf("%010.3lf|<--              %%010d\n",4321);
    printf("%-10.3lf|<--              %%-10d\n",4321);
}